#Escribe un programa que pregunte al usuario dos números y calcule su suma, resta, 
#multiplicación, división, módulo y potencia.

#pregunta los numeros
numero1 = float(input("Introduce el primero numero : "))
numero2 = float(input("introduce el segundo numero : "))

#los calculos
print("suma : " , numero1 + numero2)
print("Resta : " , numero1 - numero2)
print("Multiplicación : " , numero1 * numero2)
print("División : " , numero1 / numero2)
print("Módulo : " , numero1 % numero2)
print("Potencia : " , numero1 ** numero2)